package day8;

public class Surface {
	
	public static int calcArea(int s) {
		return s * s;
	}
	
	public static double calcArea(double s) {
		return s * s;
	}
	
	public static int calcArea(int s1, int s2) {
		return s1 * s2;
	}
	
	public static double calcArea(double s1, double s2) {
		return s1 * s2;
	}

}
